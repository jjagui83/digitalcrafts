# Calculator assigment
firstNumber = int(input("what is you first number"))
secondNumber = int(input("what is your second number"))

operand = input("what operator?")


if operand == "+":
    print(firstNumber + secondNumber)

if operand == "*":
    print(firstNumber * secondNumber)

if operand == "-":
    print(firstNumber - secondNumber)

if operand == "/":
    print(firstNumber / secondNumber)